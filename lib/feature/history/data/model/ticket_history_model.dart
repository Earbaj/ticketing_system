class TicketHistory {
  final String busName;
  final String route;
  final String date;
  final String time;
  final String seat;
  final String price;
  final String status;

  TicketHistory({
    required this.busName,
    required this.route,
    required this.date,
    required this.time,
    required this.seat,
    required this.price,
    required this.status,
  });
}
